				<div class="title">木耳红烧千张结</div>
				<div>
					<p><img src="/assets/img/recipe/article-4.jpg" width="90%" height="auto"/></p>
					<p>红烧千张结是一道素菜荤吃的家常菜，以红烧的手法烹制千张，千张吸满汤汁口感和肉类接近。美味独特，十分诱人，其菜营养丰富 味道浓郁老少皆宜。千张，北方称为豆腐皮。千张结是一种豆制食品，用很簿的豆腐皮切成条状后，手工打个结就成了，是中国家庭一种常见食品。</p>
					<p>豆腐皮中含有丰富蛋白质，而且豆腐蛋白属完全蛋白，不仅含有人体必需的8种氨基酸，而且其比例也接近人体需要，营养价值较高； 还含有的卵磷脂可除掉附在血管壁上的胆固醇，防止血管硬化，预防心血管疾病，保护心脏； 豆腐皮 含有多种矿物质，补充钙质，防止因缺钙引起的骨质疏松，促进骨骼发育，对小儿、老人的骨骼生长极为有利.</p>
					<p><br/></p>
					<p><b>食材</b></p>
 					<ul>
 						<li>千张结 300g</li>
 						<li>木耳50g</li>
 					</ul>
 					<p><br/></p>
					<p><b>配料</b></p>
 					<ul>
 						<li>八角</li>
 						<li>桂皮</li>
 						<li>油，盐，葱，姜，蒜</li>
 						<li>高汤，桂皮，干辣椒</li>
 						<li>红烧酱油，糖</li>
 					</ul>
 					<p><br/></p>
 					<p><b>步骤</b></p>
 					<ul>
 						<li>1.木耳提前泡发。</li>
 						<li>2.木耳泡发，洗净，撕开成小朵。</li>
 						<li>3.葱切段，姜切片。准备好八角桂皮 香叶 辣椒。</li>
 						<li>4.豆腐皮切成条状用手直打一个死结 。</li>
 						<li>5.锅中注入适量的油，下入八角 桂皮 香叶 辣椒爆香。</li>
 						<li>6.再下入葱姜炒出香味。</li>
 						<li>7.倒入高汤烧开。</li>
 						<li>8.加入红烧酱油 糖 盐,烧开后加入千张结和木耳。</li>
 						<li>9.转小火，盖盖子烧制汤汁浓缩即可。</li>
 					</ul>
