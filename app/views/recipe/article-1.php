				<div class="title">猴头菇炖豆腐</div>
				<div>
					<p><b>豆腐是中国的传统食品，味美而养生。调配猴头菇，味道更佳鲜美，养胃降血脂。</b></p>
					<p><img src="/assets/img/recipe/article-1.jpg" width="90%" height="auto"/></p>
					<p><br/></p>
					<p><b>食材</b></p>
 					<ul>
 						<li>豆腐 250g</li>
 						<li>猴头菇 2朵</li>
 						<li>肉末 50g</li>
 					</ul>
 					<p><br/></p>
 					<p><b>步骤</b></p>
 					<ul>
 						<li>1.干猴头菇用冷水泡发至完全膨胀变软.多漂洗几次以便去除杂质，挤净水分，撕成小块备用豆腐切成小块，在沸水中焯烫1分钟捞出控干水分备用</li>
 						<li>2.葱姜切碎碎，肉末少量备好。胡萝卜和青椒切成小粒备用</li>
 						<li>3.锅内放少许油，放入肉末炒至变色 .肉末拨到一边，放入葱蒜碎炒香</li>
 						<li>4.放入胡萝卜青椒碎炒香</li>
 						<li>5.倒入猴头菇和豆腐翻炒 .调入盐、味精、生抽、老抽，并翻炒均匀</li>
 						<li>6.添入少量水，大火煮开，转小火炖至豆腐入味，锅内只剩少量汤汁即可。</li>
 					</ul>
