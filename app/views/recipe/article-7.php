				<div class="title">榛蘑炖小鸡</div>
				<div>
					<p><img src="/assets/img/recipe/article-7.jpg" width="90%" height="auto"/></p>
					<p>小鸡炖蘑菇是一道东北名菜，市面上所有的东北菜馆几乎都提供这道菜，炖鸡的蘑菇最好选用野生的榛蘑，细杆小薄伞的那种，榛蘑可以最大程度衬托出鸡肉的鲜香，名副其实的山珍野味。东北菜中少数可以发展成为与其他高档菜系相媲美的家常菜之一。</p>
					<p><br/></p>
					<p><b>食材</b></p>
 					<ul>
 						<li>鸡腿 2个</li>
 						<li>鸡翅 2个</li>
 						<li>榛蘑 50g</li>
 					</ul>
 					<p><br/></p>
 					<p><b>步骤</b></p>
 					<ul>
 						<li>1.鸡肉洗净，榛蘑用温水浸泡。</li>
 						<li>2.葱姜干辣椒切好。  </li>
 						<li>3.鸡肉切小块冷水入锅焯烫过凉。 </li>
 						<li>4.锅中加入油烧热放入葱姜大料干辣椒炒香。</li>
 						<li>5.放入鸡肉翻炒片刻。加入料酒炒香,再加入白糖继续翻炒。</li>
 						<li>6.加入老抽。炒至鸡肉上色。加入适量的开水。</li>
 						<li>7.然后加入洗净的榛蘑。继续焖烧15分钟。</li>
 						<li>8.加盐，鸡精调味。最后淋入少许香油。</li>
 						<li>9.翻炒均匀关火。</li>
 					</ul>
 					<p><b>小提示:榛蘑要多冲洗几遍，除掉里面的泥沙灰尘口感才好。</b></p>
