				<div class="title">香辣木耳虾</div>
				<div>
					<p><img src="/assets/img/recipe/article-3.jpg" width="90%" height="auto"/></p>
					<p>虾性温味甘、微温，入肝、肾经。虾肉有、养血固精、化瘀解毒、益气滋阳、通络止痛、开胃化痰等功效配上黑木耳润燥、养血益胃、活血止血、润肺、润肠等功效  实为不错的一种食疗即美食即调理身体.</p>
					<p><br/></p>
					<p><b>食材</b></p>
 					<ul>
 						<li>大虾 450g</li>
 						<li>黑木耳 50g</li>
 					</ul>
 					<p><br/></p>
					<p><b>配料</b></p>
 					<ul>
 						<li>色拉油适量</li>
 						<li>食盐</li>
 						<li>一茶勺酱油</li>
 						<li>俩汤勺干辣椒</li>
 						<li>适量 料酒</li>
 						<li>一大勺白糖</li>
 					</ul>
 					<p><br/></p>
 					<p><b>步骤</b></p>
 					<ul>
 						<li>1.虾洗干净，沿背部剪开壳，去掉虾线。</li>
 						<li>2.木耳泡发，洗净，撕开成小朵。</li>
 						<li>3.锅内放油，炒香干辣椒。</li>
 						<li>4.放入虾，炒到皮变色。</li>
 						<li>5.放入葱花，酱油，料酒，加盖焖到虾熟。</li>
 						<li>6.放入糖调味，炒匀出锅。</li>
 					</ul>
