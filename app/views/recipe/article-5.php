				<div class="title">木耳鱼香金针菇</div>
				<div>
					<p><b>金针菇是餐桌上很常见的一种菌菇，鲜美爽口、营养丰富。 </b></p>
					<p><img src="/assets/img/recipe/article-5.jpg" width="90%" height="auto"/></p>
					<p>我们买回来以后多数会选择凉拌或者涮火锅，难免会觉得有些单调。今天用川菜独特的鱼香汁搭配金针菇，做出一道非常开胃的下饭菜，堪称是素食版的鱼香肉丝，味道浓郁、鲜辣适口，一不小心米饭就遭殃啦！</p>
					<p><br/></p>
					<p><b>食材</b></p>
 					<ul>
 						<li>金针菇 350g</li>
 						<li>黑木耳10g</li>
 					</ul>
 					<p><br/></p>
					<p><b>配料</b></p>
 					<ul>
 						<li>泡红辣椒</li>
 						<li>2个姜</li>
 						<li>2片大蒜</li>
 						<li>1瓣香葱</li>
 						<li>1颗郫县辣酱</li>
 						<li>2勺 酱油</li>
 						<li>醋,黄酒,白糖,水,淀粉各俩勺</li>
 					</ul>
 					<p><br/></p>
 					<p><b>步骤</b></p>
 					<ul>
 						<li>1.泡红椒切碎、葱、姜、蒜切末，郫县豆瓣酱剁细备用。</li>
 						<li>2.碗中调入葱花、酱油、糖、醋、酒、水淀粉拌匀成鱼香芡汁备用</li>
 						<li>3.黑木耳提前泡发洗净切丝备用。</li>
 						<li>4.将金针菇的根部切掉，用手撕成一条条的</li>
 						<li>5.锅中水烧开，把金针菇放进去焯一下，时间不要太长，半分钟即可</li>
 						<li>6.把金针菇捞出后用冷水冲凉，挤干水分。锅中放1大勺油，爆香葱、姜、蒜末和泡辣椒。</li>
 						<li>7.然后放入郫县辣豆瓣酱煸炒出香味和红油</li>
 						<li>8.把黑木耳丝放入快速翻炒下入金针菇。</li>
 						<li>9.倒入鱼香汁大火翻炒。待芡汁糊化汤汁收浓以后即可关火盛出。</li>
 					</ul>
