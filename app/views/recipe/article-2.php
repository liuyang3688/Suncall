				<div class="title">木耳姜汁五色腐竹</div>
				<div>
					<p><b>木耳姜汁五色腐竹是很适合高血压患者食用的一道养生小拌菜</b></p>
					<p><img src="/assets/img/recipe/article-2.jpg" width="90%" height="auto"/></p>
					<p>腐竹中含有丰盛卵白质，营养价值较高，其含有的卵磷脂可除去附在血管壁上的胆固醇，防止血管硬化，防备心血管疾病，保护心脏。而且还能预防骨质疏松。</p>
					<p>此菜含有丰富的蛋白质、脂肪、碳水化合物和钙、磷、铁、锌、维生素C等多种营养素，具有补气健胃、润燥、利水消肿，可治疗高血压。</p>
					<p><br/></p>
					<p><b>食材</b></p>
 					<ul>
 						<li>腐竹 150g</li>
 						<li>木耳 30g</li>
 						<li>芹菜 50g</li>
 						<li>胡萝卜 30g</li>
 						<li>红椒 30g</li>
 					</ul>
 					<p><br/></p>
 					<p><b>步骤</b></p>
 					<ul>
 						<li>1.准备好所有的食材。腐竹洗净切斜段，芹菜切段，木耳，胡萝卜，红椒全部切块。</li>
 						<li>2.锅中水烧开放入腐竹焯烫过凉。</li>
 						<li>3.再放入胡萝卜和木耳焯烫过凉。</li>
 						<li>4.所有食材沥干水分放入大碗中，加盐。</li>
 						<li>5.加白糖。加入生抽。加入香醋。</li>
 						<li>6.加入生姜汁拌均匀放置5分钟。然后加入香油。再次拌均匀即可食用。</li>
 					</ul>
