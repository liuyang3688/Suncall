				<div class="title">木耳水晶卷</div>
				<div>
					<p><img src="/assets/img/recipe/article-6.jpg" width="90%" height="auto"/></p>
					<p>水晶卷选用粉皮作卷皮，卷入馅料，白色的粉皮透明，貌似水晶，透过粉皮可以看到里面的馅料。于是，就起了个好听的菜名——水晶卷。事实上，这款水晶卷是很养生保健的。选用四种不同颜色的食材作馅料，营养很丰富，功效也很多。不但如此，水晶卷的制作方法也很健康。</p>
					<p><br/></p>
					<p><b>食材</b></p>
 					<ul>
 						<li>鸡蛋 1个</li>
 						<li>木耳50g</li>
 						<li>菠菜 200g</li>
 						<li>火腿肠 100g</li>
 						<li>粉皮一张</li>
 					</ul>
 					<p><br/></p>
 					<p><b>步骤</b></p>
 					<ul>
 						<li>1.菠菜摘洗干净，入水焯，木耳提前泡发好，焯水。</li>
 						<li>2.鸡蛋打散，摊成鸡蛋饼。</li>
 						<li>3.粉皮洗干净。</li>
 						<li>4.火腿肠切成丁。</li>
 						<li>5.鸡蛋饼切成丁。</li>
 						<li>6.焯好的木耳切成丁。</li>
 						<li>7.焯好的菠菜挤水后切成丁。</li>
 						<li>8.切好的食材放在容器中，加盐、味精、芝麻油拌均匀作水晶卷馅料</li>
 						<li>9.打开粉皮，添加馅料。我用的是白色案板，白色粉皮放在上面几乎没有什么区别。</li>
 						<li>10.粉皮卷起，裹住馅料，水晶卷就做好了。</li>
 						<li>11.切成小段，即可享用。</li>
 					</ul>
